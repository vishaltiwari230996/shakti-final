const loadSchema = () => {
    try {
        const raw = localStorage.getItem(STORAGE_KEY);
        if (raw) {
            const parsed = JSON.parse(raw);
            return {
                tables: parsed.tables || [],
                relations: parsed.relations || [],
                notes: parsed.notes || [],
                arrows: parsed.arrows || [],
            };
        }
    } catch { /* ignore */ }
    return seedSchema();
};

